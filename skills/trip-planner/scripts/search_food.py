def search_food(city: str) -> dict:
    """搜索美食推荐信息"""
    food_info = LOCAL_FOOD.get(city, {
        "must_try": ["当地特色菜"],
        "areas": ["市中心"],
        "avg_meal": {"早餐": "10-20元", "午餐": "30-50元", "晚餐": "40-80元"},
        "self_drive_tip": ""
    })

    result = {
        "city": city,
        "must_try": food_info["must_try"],
        "food_areas": food_info["areas"],
        "avg_meal_cost": food_info["avg_meal"],
        "self_drive_tip": food_info.get("self_drive_tip", ""),
        "meal_prep": get_meal_prep(city),
        "source": "AI推荐",
        "note": "价格为参考值，实际消费因餐厅档次而异"
    }

    # 尝试搜索实时推荐
    search_results = try_search_food(city)
    if search_results:
        result["search_results"] = search_results

    return result

def get_meal_prep(city: str) -> dict:
    """自驾场景下的简餐预备建议"""
    is_remote = city in ["稻城亚丁", "四姑娘山", "丹巴", "雅江", "理塘"]

    base_prep = {
        "车上常备": ["矿泉水（至少2L/人/天）", "压缩饼干/能量棒", "巧克力", "水果"],
        "服务区补给": ["泡面/自热米饭", "热饮"],
    }

    if is_remote:
        base_prep["高原特别提醒"] = [
            "高原地区水沸点低，泡面可能泡不熟，建议带自热米饭",
            "多带热水，高反时喝热水有帮助",
            "沿途餐馆少且贵，建议在县城补充足量食物",
            "带些葡萄糖冲剂，缓解高反"
        ]
        base_prep["车上常备"].extend(["自热米饭/火锅", "牛肉干", "葡萄糖冲剂"])

    return base_prep

def try_search_food(city: str) -> list:
    """尝试通过搜索引擎获取美食推荐"""
    return try_search(city, "美食")

def main():
    if len(sys.argv) < 2:
        print("用法: python search_food.py <城市>")
        sys.exit(1)

    city = sys.argv[1]
    result = search_food(city)
    print(json.dumps(result, ensure_ascii=False, indent=2))

if __name__ == "__main__":
    main()
